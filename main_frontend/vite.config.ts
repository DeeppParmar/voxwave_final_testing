import { defineConfig, loadEnv } from "vite";
import react from "@vitejs/plugin-react-swc";
import path from "path";
import { componentTagger } from "lovable-tagger";

// https://vitejs.dev/config/
export default defineConfig(({ mode }) => {
  const env = loadEnv(mode, process.cwd(), "");
  const proxyTarget = env.VITE_PROXY_TARGET || "http://localhost:8000";
  const proxyWsTarget = proxyTarget.replace(/^http:/i, "ws:").replace(/^https:/i, "wss:");

  return {
    server: {
      host: "::",
      port: 8080,
      hmr: {
        overlay: false,
      },
      proxy: {
        "/search": { target: proxyTarget, changeOrigin: true },
        "/play": { target: proxyTarget, changeOrigin: true },
        "/stream": { target: proxyTarget, changeOrigin: true },
        "/songs": { target: proxyTarget, changeOrigin: true },
        "/upload": { target: proxyTarget, changeOrigin: true },
        "/library": { target: proxyTarget, changeOrigin: true },
        "/auth": { target: proxyTarget, changeOrigin: true },
        "/me": { target: proxyTarget, changeOrigin: true },
        "/create-room": { target: proxyTarget, changeOrigin: true },
        "/room": { target: proxyTarget, changeOrigin: true },
        "/ws": { target: proxyWsTarget, ws: true, changeOrigin: true },
      },
    },
    plugins: [react(), mode === "development" && componentTagger()].filter(Boolean),
    resolve: {
      alias: {
        "@": path.resolve(__dirname, "./src"),
      },
    },
  };
});
